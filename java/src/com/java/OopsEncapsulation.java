package com.java;

public class OopsEncapsulation {
	public void studentname() {
	System.out.println("GUNASEELAN");
	}
	public void studentid() {
		System.out.println(1);
	}
	public void studentphonenumber() {
		System.out.println(9876543210l);
	}
	public void degree() {
		System.out.println("BA");
	}
	public void department() {
		System.out.println("criminology");
	}
	public void year() {
		System.out.println("3rd YEAR");
	}
	public void bloodgroup() {
		System.out.println("A+ve");
	}
	public void dateofbirth() {
		System.out.println("01/02/1995");
	}
	public static void main(String [] arg) {
		OopsEncapsulation s=new OopsEncapsulation();
		s.studentname();
		s.studentid();
		s.studentphonenumber();
		s.degree();
		s.department();
		s.year();
		s.bloodgroup();
		s.dateofbirth();
	}
}
